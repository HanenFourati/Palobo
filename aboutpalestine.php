<font color="Green">RELATIONSHIPS</font><br>
When two Palestinians greet one another they usually shake hands. It is also common for two women to kiss one another on the cheeks in greeting.

Palestinians are known for their hospitality. Neighbors have very friendly relations and look out for one another's interests. 
Because Palestinians tend to stay in one house or apartment for their entire lives, neighbors establish lifelong relationships.<br><br>
