<font color="Green">LANGUAGE </font><br>
Palestinians speak Arabic. "Hello" in Arabic is marhaba or ahlan, to which one replies, marhabtayn or ahlayn.
Other common greetings are As-salam alaykum (Peace be with you), with the reply of wa 'alaykum as salam (and to you peace).
 Ma'assalama means "goodbye," with the literal translation being "go with peace." "Thank you" is Shukran, and "You're welcome" is 'Afwan. "Yes" is na'am,
 and "no" is la'a. The numbers one to ten in Arabic are: wahad, ithnayn, thalatha, arba'a, khamsa, sita, sab'a, thamanya, tis'a, and 'ashara.

Common names for boys are Ahmad, Shukri, Ismàil, and Ibrahim. Muhammad is a very common Muslim name. 
Hanna is a very common Christian name. Ìsa (Jesus) is used by both Muslims and Christians. Common names for girls are Samia, Sawsan, Maysoon, Muna, and Fatima. 
On rare occasions girls are given politically significant names such as Al-Quds (Jerusalem).
