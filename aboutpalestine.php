<font color="Green">FOOD</font><br>
Palestinians eat typical Middle Eastern food, such as falafel (deep-fried chickpea balls or patties), hummus (ground chickpeas with garlic, lemon juice), tahini (a sesame paste), lamb, chicken, rice, nuts, and eggplant. A favorite Palestinian candy is halvah, a sweet nougat made of sesame seeds and honey. For eating meals, some rural Palestinians sit on mats or cushions around a cloth laid on the floor and scoop up their food with pieces of pita bread, called khubz. They drink lots of strong black Turkish coffee.<br>

