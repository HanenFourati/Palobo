<font color="Green">TRADITIONAL CLOTHING</font> <BR>
Palestine displays a wide range of clothing styles depending on locality, whether the person is a villager or Bedouin or townsperson. Foreign travelers to Palestine in the 19th and early 20th centuries often commented on the rich variety of the costumes worn, particularly by the fellaheen or village women.
Basic dress:
<ul>
  <li>Thob: loose fitting robe with sleeves, the actual cut of the garment varied by region.</li>
  <li>Libas: pants.
  <ul>
   <li>Qabbeh: the square chest panel of the Thob, often decorated. </li>
   <li>Diyal: brocaded back hem panel on the Bethlehem dress. </li>
   <li>Shinyar: lower back panel of the dress, decorated in some regions.</li></ul>
  </li>
   
  <li>Taqsireh: short embroidered jacket worn by the women of Bethlehem on festive occasions. 
  The gold couching of the jackets often matched the dress. Simpler jackets were used over everyday dresses.
  The name is derived from the Arabic verb "to shorten", (Stillmann, p. 36).</li>
  <li>Jubbeh: jacket, worn by men and women.</li>
  <li>Jillayeh: embroidered jubbeh, often the embroidered outer garment of a wedding costume.</li>
  <li>Shambar: large veil, common to the Hebron area and southern Palestine.
</li>
</ul>

